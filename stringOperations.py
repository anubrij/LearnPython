#split function
def break_words(sentence):
    """this will split the sentance with space and create a list"""
    return sentence.split(' ')

#Sort the words in sentence
def sort_words(sentence):
    """sort the charector in string or values in list"""
    return sorted(sentence)

def join_words(words):
    """It will join the list with space"""
    return " ".join(words)
